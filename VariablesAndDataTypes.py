print("\n\nCombine 2 strings:      string1 + string2")
phrase1 = "The first part of the phrase, "
phrase2 = "is not the second part."
print("phrase1 = " + phrase1)
print("phrase2 = " + phrase2)
print("phrase1 + phrase2 = " + phrase1 + phrase2)
# ====================================================================================

print("\n\nChange string to all upper/lower case")
uppercase_phrase = "THIS PHRASE IS ALL UPPERCASE"
lowercase_phrase = "this phrase is all lowercase"
print("uppercase_phrase = " + uppercase_phrase)
print("lowercase_phrase = " + lowercase_phrase)
print("With    .lower() = " + uppercase_phrase.lower())
print("With    .upper() = " +lowercase_phrase.upper())
# ====================================================================================

print("\n\nCheck if string is upper/lowercase")
print("Remember, anything not a string (bool here) needs to be converted to print WITH a string.")
print("uppercase_phrase.isupper() = " + str(uppercase_phrase.isupper()))
print("uppercase_phrase.islower() = " + str(uppercase_phrase.islower()))

# ====================================================================================

print("\n\nRun 2 built in functions at once")
print("uppercase_phrase.lower().isupper() = " + str(uppercase_phrase.lower().isupper()))
print("We get False.  uppercase phrase, changed to lower, then checked for upper.")
# ====================================================================================

print("\n\nWhat is the length of the phrase?")
print("len(uppercase_phrase) = " + str(len(uppercase_phrase)))
